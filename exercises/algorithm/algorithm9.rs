use std::cmp::Ord;
use std::default::Default;

pub struct Heap<T>
where
    T: Default,
{
    items: Vec<T>,
    comparator: fn(&T, &T) -> bool,
    ptr:usize,
}

impl<T> Heap<T>
where
    T: Default,
{
    pub fn new(comparator: fn(&T, &T) -> bool) -> Self {
        Self {
            items: vec![],
            comparator,
            ptr:0
        }
    }

    pub fn len(&self) -> usize {
        self.items.len()
    }

    pub fn is_empty(&self) -> bool {
        self.len() == 0
    }

    pub fn add(&mut self, value: T) {
        self.items.push(value);
        self.up(self.len()-1)
    }
    //   0 
    // 1   2
    //3 4 5 6
    fn up(&mut self,mut j:usize) {
        loop {
            if j == 0 {
                return;
            }
            let i = (j - 1) / 2; // parent
            if i == j || ! (self.comparator)(&self.items[j], &self.items[i]) {
                break
            }else {
                self.ptr = 0;
            }
            self.items.swap(i, j);
            j = i
        }
    }
}

impl<T> Heap<T>
where
    T: Default + Ord,
{
    /// Create a new MinHeap
    pub fn new_min() -> Self {
        Self::new(|a, b| a < b)
    }

    /// Create a new MaxHeap
    pub fn new_max() -> Self {
        Self::new(|a, b| a > b)
    }
}

